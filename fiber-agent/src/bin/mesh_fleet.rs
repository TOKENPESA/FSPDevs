use std::collections::HashMap;
use std::time::Duration;

use fiber_agent_sidecar::{
    parse_fleet_range, run_agent_sidecar, agent_fnn_pubkey, SidecarConfig, RING_SIZE,
};
use tokio::time::sleep;

#[tokio::main]
async fn main() {
    let (from, to) = match parse_fleet_range() {
        Ok(range) => range,
        Err(e) => {
            eprintln!("❌ [MESH FLEET] {e}");
            return;
        }
    };

    let count = to.saturating_sub(from).saturating_add(1);
    let mut config = SidecarConfig::from_env();
    config.force_simulate_fnn = std::env::var("MESH_FLEET_LIVE_FNN")
        .map(|v| !(v == "1" || v.eq_ignore_ascii_case("true")))
        .unwrap_or(true);
    if config.force_simulate_fnn {
        std::env::set_var("FNN_MODE", "simulate");
    } else {
        std::env::set_var("MESH_FNN_AUTO_PORTS", "true");
    }

    if let Ok(path) = std::env::var("MESH_PUBKEY_REGISTRY_PATH") {
        if let Err(e) = write_sim_pubkey_registry(&path, from, to) {
            eprintln!("⚠️ [MESH FLEET] Could not write mesh pubkeys: {e}");
        } else {
            println!("📒 [MESH FLEET] Wrote {count} pubkey(s) to {path}");
        }
    }

    let stagger_ms: u64 = std::env::var("MESH_FLEET_STAGGER_MS")
        .ok()
        .and_then(|s| s.parse().ok())
        .unwrap_or(15);

    println!(
        "🚀 [MESH FLEET] Starting {count} sidecar(s) with embedded FNN (FA-{from}..={to}) — MFA ws://127.0.0.1:1025/ws/{{id}}"
    );
    if config.force_simulate_fnn {
        println!("   Each FA uses in-process SimulatedFnnClient (one FNN identity per agent).");
    } else {
        println!("   Live FNN mode: set MESH_FNN_AUTO_PORTS=true and spawn fnn-testnet nodes first.");
    }

    for agent_id in from..=to {
        let cfg = config.clone();
        tokio::spawn(async move {
            run_agent_sidecar(agent_id, cfg).await;
        });
        if agent_id % 32 == 0 {
            sleep(Duration::from_millis(stagger_ms)).await;
        }
    }

    println!("✅ [MESH FLEET] All {count} agents spawned. Ctrl+C to stop.");

    loop {
        sleep(Duration::from_secs(3600)).await;
    }
}

fn write_sim_pubkey_registry(path: &str, from: u16, to: u16) -> Result<(), String> {
    let mut map: HashMap<String, String> = HashMap::new();
    map.insert(
        "_comment".to_string(),
        "Auto-generated by mesh-fleet-daemon — secp256k1 pubkey per FA (dev key)".to_string(),
    );
    for id in from..=to.min(RING_SIZE) {
        map.insert(id.to_string(), agent_fnn_pubkey(id));
    }
    let json = serde_json::to_string_pretty(&map).map_err(|e| e.to_string())?;
    std::fs::write(path, json).map_err(|e| e.to_string())
}
